package 数组;

public class 数组的动态初始化 {
    public static void main(String[] args) {
        //数组的类型[] 数组的名称=new 数组的类型[数组的长度]
        //定义一个int型可以存放20个数据的数组
        int[] a1=new int[20];

        //定义一个Double型可以存放30个数据的数组
        Double[] a2=new Double[30];

        //定义一个String型可以存放40个数据的数组
        String[] a3=new String[40];

        //顶一个boolean型可以存放50个数据的数组
        boolean[] a4=new boolean[50];
    }
}
